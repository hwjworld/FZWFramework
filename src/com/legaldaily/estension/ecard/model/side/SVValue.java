package com.legaldaily.estension.ecard.model.side;

public class SVValue {
	private int id;
	private String name;
	private String title;
	private String value;
	private String isGroupSetting;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getIsGroupSetting() {
		return isGroupSetting;
	}
	public void setIsGroupSetting(String isGroupSetting) {
		this.isGroupSetting = isGroupSetting;
	}


}
